package org.catamarancode.faq.util;

public class CatamaranMarkdownException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public CatamaranMarkdownException() {
	}

	public CatamaranMarkdownException(String arg0) {
		super(arg0);
	}

	public CatamaranMarkdownException(Throwable arg0) {
		super(arg0);
	}

	public CatamaranMarkdownException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

}
